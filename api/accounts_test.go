func randomAccount(owner string) db.Account {
	return db.Account{
		ID:       util.RandomInt(1, 1000),
		Owner:    owner,
		Balance:  util.RandomMoney(-99999, 99999),
		Currency: util.RandomCurrency(),
	}
}

func randomAccountWithMinAmountAndCurrency(owner string, minAmount int64, currency string) db.Account {
	if minAmount >= math.MaxInt64 {
		minAmount = math.MaxInt64 / 2
	}

	return db.Account{
		ID:       util.RandomInt(1, 1000),
		Owner:    owner,
		Balance:  util.RandomMoney(minAmount, math.MaxInt64),
		Currency: currency,
	}
}

